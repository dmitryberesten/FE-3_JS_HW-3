// 1. Записати в окремих змінних кожне слово: My, name, is.
// 2. Поєднати всі слова в змінну fullName.
// 3. До змінної fullName додати ‘Viktor’.
// 4. fullName вивести в консоль.

// 1. Створюємо окремі змінні для кожного слова
let word1 = "My";
let word2 = "name";
let word3 = "is";

// 2. Поєднуємо всі слова в змінну fullName
let fullName = word1 + " " + word2 + " " + word3;

// 3. Додаємо до змінної fullName слово 'Viktor'
fullName += " Viktor";

// 4. Виводимо значення змінної fullName в консоль
console.log(fullName);
